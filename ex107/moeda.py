def aumentar(valor,porc):
    aumento = valor + (valor * porc / 100)
    return aumento 


def diminuir(valor,porc):
    reduc = valor - (valor * porc / 100 )   
    return reduc


def dobro(valor):
    mult = valor * 2
    return mult

def metade(valor):   
    divid = valor / 2
    return divid


    
